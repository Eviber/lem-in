#include "ft_printf.h"

int		ft_isattribute(char c)
{
	if (c == '#' || c == '+' || c == '-' || c == '0' || c == ' ')
		return (1);
	return (0);
}

int		ft_islenmod(char c)
{
	if (c == 'h' || c == 'l' || c == 'j' || c == 'z')
		return (1);
	return (0);
}
