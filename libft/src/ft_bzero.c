#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*tmp;

	if (n > 0)
	{
		tmp = (char *)s;
		while (n)
		{
			*tmp = '\0';
			tmp++;
			n--;
		}
	}
}
